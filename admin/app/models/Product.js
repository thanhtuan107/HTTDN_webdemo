class Wine {
    constructor(id, name, type, origin, volume, alcoholContent, price, image, description) {
        this.id = id;
        this.name = name;
        this.type = type;
        this.origin = origin;
        this.volume = volume;
        this.alcoholContent = alcoholContent;
        this.price = price;
        this.image = image;
        this.description = description;
    }
}

export default Wine;